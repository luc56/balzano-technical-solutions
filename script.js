function setLanguage(lang) {
    localStorage.setItem('preferred_lang', lang);
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang][key]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = translations[lang][key];
            } else {
                el.innerHTML = translations[lang][key];
            }
        }
    });

    // Update active state of buttons
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
    });
    // Re-create icons if they were inside translated elements (unlikely but safe)
    lucide.createIcons();
}

// Language switcher event listeners
document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => {
        setLanguage(btn.getAttribute('data-lang'));
    });
});

// Initial Language Setup
const savedLang = localStorage.getItem('preferred_lang') ||
    (navigator.language.startsWith('de') ? 'de' :
        navigator.language.startsWith('en') ? 'en' : 'it');
setLanguage(savedLang);

// Initialize Lucide Icons
lucide.createIcons();

// Reveal Animations on Scroll
const revealElements = document.querySelectorAll('[data-reveal]');

const revealObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            entry.target.classList.add('active');
        }
    });
}, {
    threshold: 0.1
});

revealElements.forEach(el => revealObserver.observe(el));

// Navbar shadow on scroll
const navbar = document.querySelector('.navbar');
window.addEventListener('scroll', () => {
    if (window.scrollY > 50) {
        navbar.style.boxShadow = '0 10px 30px rgba(0, 0, 0, 0.5)';
        navbar.style.background = 'rgba(13, 15, 18, 0.95)';
    } else {
        navbar.style.boxShadow = 'none';
        navbar.style.background = 'rgba(13, 15, 18, 0.8)';
    }
});

// Smooth scroll for nav links
document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function (e) {
        e.preventDefault();
        const target = document.querySelector(this.getAttribute('href'));
        if (target) {
            window.scrollTo({
                top: target.offsetTop - 80,
                behavior: 'smooth'
            });
        }
    });
});

// Active Nav Link highlight on scroll
const sections = document.querySelectorAll('section');
const navItems = document.querySelectorAll('.nav-links a[href^="#"]');

window.addEventListener('scroll', () => {
    let current = '';

    sections.forEach(section => {
        const sectionTop = section.offsetTop;
        if (window.scrollY >= (sectionTop - 200)) {
            current = section.getAttribute('id');
        }
    });

    navItems.forEach(link => {
        link.classList.remove('active');
        if (current && link.getAttribute('href') === `#${current}`) {
            link.classList.add('active');
        }
    });
});

// Privacy Modal Logic
const modal = document.getElementById("privacy-modal");
const trigger = document.getElementById("privacy-trigger");
const closeBtn = document.querySelector(".close-modal");
const acceptBtn = document.getElementById("privacy-accept-btn");

function closePrivacyModal() {
    if (modal) {
        modal.style.display = "none";
        localStorage.setItem('privacy_accepted', 'true');
    }
}

// Show on first visit
if (!localStorage.getItem('privacy_accepted') && modal) {
    modal.style.display = "block";
}

// Footer link opens it manually
if (trigger) {
    trigger.onclick = function () {
        if (modal) modal.style.display = "block";
    }
}

// Close via X button
if (closeBtn) {
    closeBtn.onclick = closePrivacyModal;
}

// Close via accept button
if (acceptBtn) {
    acceptBtn.onclick = closePrivacyModal;
}

// Close via click outside
window.onclick = function (event) {
    if (event.target == modal) {
        closePrivacyModal();
    }
}
